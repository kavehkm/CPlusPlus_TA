#include <iostream>
using namespace std;


int main () {
    int x = 2;
    int y = 5;

    cout << x << ' + ' << y << ' = ' << x + y << endl;
    cout << x << ' - ' << y << ' = ' << x - y << endl;
    cout << x << ' * ' << y << ' = ' << x * y << endl;   
    cout << x << ' / ' << y << ' = ' << x / y << endl;
    cout << x << ' % ' << y << ' = ' << x % y;

    return 0;
}