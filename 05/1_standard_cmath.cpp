#include <iostream>
#include <cmath>
using namespace std;


int main () {
    // abs
    cout << "abs(8) = " << abs(8) << endl;
    cout << "abs(-8) = " << abs(-8) << endl;

    // floor and ceil
    cout << "floor(4.3) = " << floor(4.3) << endl;
    cout << "ceil(4.3) = " << ceil(4.3) << endl;
    
    // log and log10
    cout << "log(100) = " << log(100) << endl;
    cout << "log10(100) = " << log10(100) << endl;

    // exp and pow
    cout << "exp(5) = " << exp(5) << endl;
    cout << "pow(2, 3) = " << pow(2, 3) << endl;

    // sqrt
    cout << "sqrt(100) = " << sqrt(100) << endl;

    return 0;
}