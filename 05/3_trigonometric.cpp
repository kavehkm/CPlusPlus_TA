#include <iostream>
#include <cmath>
using namespace std;


int main () {
    const double PI = 3.1415926535;
    double degree = PI / 4;

    // finding the sin, cos, and tan of an angel of 45 degree
    cout << "sin(45): " << sin(degree) << endl;
    cout << "cons(45): " << cos(degree) << endl;
    cout << "tan(45): " << tan(degree) << endl;
    return 0;
}