#include <iostream>
using namespace std;


int main() {
    cout << "size of `char` is: " << sizeof(char) << " byte" << endl;
    cout << "size of `short int` is: " << sizeof(short int) << " bytes" << endl;
    cout << "size of `int` is: " << sizeof(int) << " bytes" << endl;
    cout << "size of `long int` is: " << sizeof(long int) << " bytes" << endl;

    return 0;
}